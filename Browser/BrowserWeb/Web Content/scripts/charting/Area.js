// Constructor.
//***
// --------- Area ------------
//  This class represents an area in the svg canvas.
//  Other object can utilise an area to draw upon.
// 
//****

function Area( svgParent, x, y, width, height)
{
// -----------------Variables-----------------------------------------------------

    // The parent node in DOM.
    this.svgParent = svgParent;
    // X coordindate of the location of this area
    this.x = x;
    // Y coordindate of the location of this area
    this.y = y;
    // Width of this area.
    this.width = width;
    // Height of this area.
    this.height = height;
    
// -----------------Methods--------------------------------------------------------


// -----------------Calls to initial internal routines-----------------------------    
}


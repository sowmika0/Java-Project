package com.temenos.t24browser.exceptions;

public class SecurityViolationException extends Exception {

	public SecurityViolationException() {
	}

	public SecurityViolationException(String arg0) {
		super(arg0);
	}

	public SecurityViolationException(Throwable arg0) {
		super(arg0);
	}

	public SecurityViolationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
